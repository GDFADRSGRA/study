#include<stdio.h>
int main(){
//除法（/）
printf("%d\n",10%3);
//取余（%）
printf("%d\n",10%2);
printf("%d\n",10/2);
//1.整数计算，结果一定是一个整数
printf("%d\n",3+2);
printf("%d\n",10/3);
//2.小数计算，结果一定是一个小数
printf("%f\n",10.1+3.0);
//3.整数和小数计算，结果一定是一个小数
printf("%lf\n",10/3.0);
//4.小数直接参与运算，结果可能不准确
printf("%.20lf\n",10.0/3.0);
//除法的细节：
 //1.不能除以0
//取余的细节：
 //1.预算的数据必须全部是整数
 //2.获取余数的正负，是跟第一个数字保持一致的
 //3.不能除以0 
return 0;
}
