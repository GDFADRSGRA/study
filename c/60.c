#include<stdio.h>
int main()
{   
    /*
       找出0~1000之内，符合要求的数字
       要求：每一位的数字之和等于15、
       举例：78，168，1167

       套路：
           1.外循环：获取范围中的每一个数字0~1000
             i:1000
           2.内循环：处理这个数字
                        每一位数字之和等于15  
    */ 

     printf("0~1000内各位数字之和等于15的数有：\n");
    
      // 初始化循环变量
    int num = 0;
    // 外层while循环遍历0~1000的所有数
    while (num <= 1000) 
    {
        int sum = 0;
        int temp = num;
        
        // 内层while循环计算当前数的各位数字之和
        while (temp > 0) 
        {
            sum += temp % 10;  // 取最后一位数字
            temp /= 10;        // 去掉最后一位数字
        }
        
        // 判断是否符合条件，符合则输出
        if (sum == 15) 
        {
            printf("%d ", num);
        }
        
        num++;  // 循环变量自增，避免死循环
    }
    
    printf("\n");
    return 0;
}     